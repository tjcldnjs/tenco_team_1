package com.tenco.temp1;

public class Place {
	private String name;
	private String spot;
	private String gym;
	
	
	public Place(String name,String spot) {
		this.name=name;
		this.spot=spot;
	}
	

	
	
	
	
	
	
	
	
	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSpot() {
		return spot;
	}

	public void setSpot(String spot) {
		this.spot = spot;
	}

	public String getGym() {
		return gym;
	}

	public void setGym(String gym) {
		this.gym = gym;
	}
	
}
